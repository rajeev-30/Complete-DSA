package BIT_MANIPULATION;

public class first {
    public static void main(String[] args) {
        System.out.println(5 & 6);
        System.out.println(5 | 6);
        System.out.println(5 ^ 6);
        System.out.println(~5);
        System.out.println(7>>1);
        System.out.println(7<<1);
    }
}
 